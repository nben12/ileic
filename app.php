        <h1>Présentation du Groupe ILEIC</h1>
        <p>
        Le groupe ILEIC est un groupe d’écoles privées de formation professionnelle, autorisées et accréditées par l’Etat. Notre mission est de préparer techniquement des jeunes qui seront les acteurs de l’activité économique.

Notre ambition est de répondre aux attentes des entreprises en quête de personnes qualifiées dans les domaines des nouvelles technologies de l’information et de la communication. Mais aussi dans le domaine de Gestion, de Management, de Finance, Logistiques, Ressources Humaines ...

Tous les diplômes du Groupe ILEIC sont des diplômes d'Etat : accrédités et reconnus par l'Etat.
        </p>
        <p>
        Le Groupe ILEIC a été créé depuis 1988 par la volonté approuvée par ses fondateurs: Deux frères professeurs universitaires et professionnels de l’entreprise.

L’enseignement à ILEIC est dispensé par une équipe pédagogique jeune est dynamique formée d’enseignants hautement qualifiés émanant de l’environnement propice à l’enseignement théorique et pratique. Des experts professionnels veillent à l’adaptation et la mise à jour des programmes
 des filières et Diplômes en fonction de la demande excessive du milieux professionnel.
        </p>
